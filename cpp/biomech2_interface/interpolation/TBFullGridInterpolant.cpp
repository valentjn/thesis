    void TBFullGridInterpolant::eval(const sgpp::base::DataVector& x,
                                   sgpp::base::DataVector& value) {
      if ((x[0] < 0.0) || (x[0] > 1.0) || (x[1] < 0.0) || (x[1] > 1.0) ||
          (x[2] < 0.0) || (x[2] > 1.0)) {
        value.setAll(INFINITY);
        return;
      }

      evalCounter++;

      const double theta = x[0] * (THETA_MAX - THETA_MIN) + THETA_MIN;
      const double alphaT = x[1] * (ALPHA_MAX - ALPHA_MIN) + ALPHA_MIN;
      const double alphaB = x[2] * (ALPHA_MAX - ALPHA_MIN) + ALPHA_MIN;

      value[0] = intpT(theta, alphaT);
      value[1] = intpB(theta, alphaB);
    }

    void TBFullGridInterpolant::clone(
        std::unique_ptr<sgpp::optimization::VectorFunction>& clone) const {
      clone = std::unique_ptr<sgpp::optimization::VectorFunction>(
          new TBFullGridInterpolant(*this));
    }

    size_t TBFullGridInterpolant::getEvalCounter() const {
      return evalCounter;
    }

